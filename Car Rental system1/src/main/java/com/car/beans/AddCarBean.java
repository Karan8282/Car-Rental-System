package com.car.beans;

public class AddCarBean {
	
	private String carName;
	private String carNo;
	private String noOfSeats;
	private String carColor;
	private String modelYear;
	private String fuelType;
	private String carDocuments;
	private String carRent;
	
	public String getCarName() {
		return carName;
	}
	public void setCarName(String carName) {
		this.carName = carName;
	}
	public String getCarNo() {
		return carNo;
	}
	public void setCarNo(String carNo) {
		this.carNo = carNo;
	}
	public String getNoOfSeats() {
		return noOfSeats;
	}
	public void setNoOfSeats(String noOfSeats) {
		this.noOfSeats = noOfSeats;
	}
	public String getCarColor() {
		return carColor;
	}
	public void setCarColor(String carColor) {
		this.carColor = carColor;
	}
	public String getModelYear() {
		return modelYear;
	}
	public void setModelYear(String modelYear) {
		this.modelYear = modelYear;
	}
	public String getFuelType() {
		return fuelType;
	}
	public void setFuelType(String fuelType) {
		this.fuelType = fuelType;
	}
	public String getCarDocuments() {
		return carDocuments;
	}
	public void setCarDocuments(String carDocuments) {
		this.carDocuments = carDocuments;
	}
	public String getCarRent() {
		return carRent;
	}
	public void setCarRent(String carRent) {
		this.carRent = carRent;
	}
	

}
